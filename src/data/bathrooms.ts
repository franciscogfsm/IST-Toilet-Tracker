// Helper functions for filtering
export const getUniqueFloors = (): string[] => {
  const floors = bathrooms.map((bathroom) => bathroom.floor);
  return [...new Set(floors)].sort((a, b) => {
    // Custom sort: negative numbers first, then 0, then positive
    const numA = parseInt(a);
    const numB = parseInt(b);
    return numA - numB;
  });
};

export const getUniqueBuildings = (): string[] => {
  const buildings = bathrooms.map((bathroom) => bathroom.building);
  return [...new Set(buildings)].sort();
};

export const getBathroomsByFloor = (floor: string): Bathroom[] => {
  return bathrooms.filter((bathroom) => bathroom.floor === floor);
};

export const getBathroomsByBuilding = (building: string): Bathroom[] => {
  return bathrooms.filter((bathroom) => bathroom.building === building);
};
